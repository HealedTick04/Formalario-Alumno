    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        if (alumnos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay alumnos registrados.", "Información", JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    
    // Construir el mensaje con los detalles de todos los alumnos
    StringBuilder mensaje = new StringBuilder("Lista de Alumnos:\n");
    for (Alumno alumno : alumnos) {
        mensaje.append("\nNúmero de control: ").append(alumno.getNumControl())
               .append("\nNombre: ").append(alumno.getNombre())
               .append("\nApellido paterno: ").append(alumno.getApellidoPaterno())
               .append("\nApellido materno: ").append(alumno.getApellidoMaterno())
               .append("\nGénero: ").append(alumno.getGenero())
               .append("\nCarrera: ").append(alumno.getCarrera())
               .append("\n--------------------------\n");
    }
    
    // Mostrar el mensaje con los detalles de todos los alumnos
    JOptionPane.showMessageDialog(this, mensaje.toString(), "Alumnos Registrados", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_btnMostrarActionPerformed

