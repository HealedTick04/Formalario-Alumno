    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        int numControl;
        String nombre;
        String apellidoPaterno;
        String apellidoMaterno;
        char genero = '\0';
        String carrera = null;
        optGenero.add(optFemenino);
        optGenero.add(optMasculino);
        
        if (!optFemenino.isSelected() && !optMasculino.isSelected()) {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un género.", "Error", JOptionPane.ERROR_MESSAGE);
        return; 
    }
    
    do {
        numControl = Integer.parseInt(txtNumControl.getText());
        if (!validar.validarNumControl(numControl)) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un numero de control válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } while (!validar.validarNumControl(numControl));
    
    do {
        nombre = txtNombre.getText();
        if (!validar.validarNombreApellido(nombre)) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    } while (!validar.validarNombreApellido(nombre));
    
 
    do {
        apellidoPaterno = txtAperllidoPaterno.getText();
        if (!validar.validarNombreApellido(apellidoPaterno)) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un apellido paterno válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
    } while (!validar.validarNombreApellido(apellidoPaterno));
    
    
    do {
        apellidoMaterno = txtApellidoMaterno.getText();
        if (!validar.validarNombreApellido(apellidoMaterno)) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un apellido materno válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
    } while (!validar.validarNombreApellido(apellidoMaterno));
    
    if (optFemenino.isSelected()) {
        genero = 'F';
    } else if (optMasculino.isSelected()) {
        genero = 'M';
    }
    carrera = cboCarreras.getSelectedItem().toString();
    Alumno alumno = new Alumno(numControl, nombre, apellidoPaterno, apellidoMaterno,genero,carrera);
    alumnos.add(alumno);
    txtNumControl.setText("");
    txtNombre.setText("");
    txtAperllidoPaterno.setText("");
    txtApellidoMaterno.setText("");

    }//GEN-LAST:event_btnAceptarActionPerformed

